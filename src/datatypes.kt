fun main() {

    var num : Int = -5 //int
    println(num)

    var myNumber : Float = 56.7F //float
    println(myNumber)

    var temperature : Double = 45.5 //Double
    println(temperature)

    var grade : Char= 'A' //Character
    println(grade)

    var language : String= "Kotlin" //String
    println(language)

    var isMammal : Boolean= true //Boolean
    println(isMammal)

}